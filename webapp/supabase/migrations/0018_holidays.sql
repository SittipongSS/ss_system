-- 0018_holidays.sql
-- ระบบวันที่/วันทำการ: ตาราง holidays = วันหยุด (วันที่ไม่ทำการ) สำหรับให้
-- PM timeline นับวันทำการได้ถูกต้อง (เสาร์/อาทิตย์ถือเป็นหยุดเสมอจาก logic
-- isBusinessDay อยู่แล้ว — ตารางนี้เก็บเฉพาะวันหยุดนักขัตฤกษ์/บริษัทเพิ่มเติม).
--
-- shared-core: PM (gen/recalc timeline) อ่านชุดวันหยุดจากตารางนี้; ถ้าตารางว่าง/
-- อ่านไม่ได้ จะ fallback ไป THAI_HOLIDAYS (hardcode ใน lib/pm/dateHelpers.js).
--
-- ⚠ รันมือบน Supabase SQL Editor (DDL ผ่าน service-role/PostgREST ไม่ได้ — เหมือน 0005-0017).

create table if not exists holidays (
  date       text primary key,          -- 'YYYY-MM-DD' (วันหยุด)
  name       text,                       -- ชื่อวันหยุด (เช่น "วันสงกรานต์")
  "createdAt" timestamptz default now()
);

-- RLS: เปิดไว้ ไม่มี policy (เข้าผ่าน service-role เท่านั้น เหมือนตารางอื่น)
alter table holidays enable row level security;

